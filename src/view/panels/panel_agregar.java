    private void mostrarDatos() {

        ServicioDAO listar = new ServicioDAO();

        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("Titulo");
        agregar_table.setModel(table);

        ArrayList<ServicioModel> servicios = listar.listarDatos();

        String[] datos = new String[1];

        //Pasa los datos del ArrayList a un String[] y los lista en la tabla
        for (int i = 0; i < servicios.size(); i++) {
            datos[0] = servicios.get(i).getTitulo();
            table.addRow(datos);
        }
    }

    public static ClienteModel recuperar() throws IOException, ClassNotFoundException {
        //Recupera el username del cliente
        ArchiCliente rec = new ArchiCliente();
        ClienteModel recuperar = rec.recuperar();
        return recuperar;
    }

