    private void agregar_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_agregar_tableMouseClicked

        //Obtiene el titulo de la fila seleccionada en la tabla
        seleccionado = agregar_table.rowAtPoint(evt.getPoint());
        tituloSeleccionado = String.valueOf(agregar_table.getValueAt(seleccionado, 0));
    }//GEN-LAST:event_agregar_tableMouseClicked

    private void btn_agregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agregarMouseClicked
        
        
        ServicioDAO obtenerId = new ServicioDAO();
        TransmisionDAO agregar = new TransmisionDAO();
        LocalDate date = LocalDate.now(); //Obtiene la fecha actual
        LocalTime hora = LocalTime.now(); // obtiene la hora actual
        String fecha = date.toString() + " " + hora.toString(); // concatena fecha y hora y los convierte en String 

        int id = obtenerId.obtenerIdServicio(tituloSeleccionado);

        agregar.agregar(user, id, fecha);

        JOptionPane.showMessageDialog(this, "Se agrego con exito " + tituloSeleccionado + " a escuchados recientemente");

    }//GEN-LAST:event_btn_agregarMouseClicked


