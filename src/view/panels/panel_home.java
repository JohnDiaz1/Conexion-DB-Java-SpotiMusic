    private void mostrarDatos() {

        TransmisionDAO listar = new TransmisionDAO();

        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("Titulo");
        table.addColumn("Fecha");
        historial_table.setModel(table);

        ArrayList<TransmisionModel> servicios = listar.listarDatos(user);

        String[] datos = new String[2];

        for (int i = 0; i < servicios.size(); i++) {
            datos[0] = servicios.get(i).getServicio();
            datos[1] = servicios.get(i).getFecha();
            table.addRow(datos);
        }

    }

    public static ClienteModel recuperar() throws IOException, ClassNotFoundException {
        ArchiCliente rec = new ArchiCliente();
        ClienteModel recuperar = rec.recuperar();
        return recuperar;
    }

