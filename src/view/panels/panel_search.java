    public void mostrarResultadoCancion() {

        BusquedaDAO busqueda = new BusquedaDAO();

        change_resenia.setText("RESEÑA:");
        change_anio.setText("AÑO:");
        change_cantante.setText("CANTANTE:");

        ArrayList<CancionModel> cancion = busqueda.buscarCancion(search_field.getText());

        if (cancion.get(0).getTitulo().equals("No se encontro")) {
            JOptionPane.showMessageDialog(this, "No se encontro la cancion con titulo: " + search_field.getText());
        } else {

            txt_titulo.setText(cancion.get(0).getTitulo());
            txt_resenia.setText("<html> " + cancion.get(0).getResenia() + " <html>");
            txt_anio.setText(String.valueOf(cancion.get(0).getAnio()));
            txt_cantante.setText(cancion.get(0).getCantante());
        }
    }

    public void mostrarResultadoPodcast() {

        BusquedaDAO busqueda = new BusquedaDAO();

        change_resenia.setText("EPISODIOS:");
        change_anio.setText("TEMPORADAS:");
        change_cantante.setText("");

        ArrayList<PodcastModel> podcast = busqueda.buscarPodcast(search_field.getText());

        if (podcast.get(0).getTitulo().equals("No se encontro")) {
            JOptionPane.showMessageDialog(this, "No se encontro el podcast con titulo: " + search_field.getText());
        } else {

            txt_titulo.setText(podcast.get(0).getTitulo());
            txt_resenia.setText(String.valueOf(podcast.get(0).getEpisodios()));
            txt_anio.setText(String.valueOf(podcast.get(0).getTemporadas()));
        }
    }

