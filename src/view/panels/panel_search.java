    private void cbo_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbo_searchActionPerformed
        // TODO add your handling code here: 
        String searchType = cbo_search.getSelectedItem().toString();

        txt_titulo.setText("");
        txt_resenia.setText("");
        txt_anio.setText("");
        txt_cantante.setText("");

        switch (searchType) {
            case "Canción":
                change_resenia.setText("RESEÑA:");
                change_anio.setText("AÑO:");
                change_cantante.setText("CANTANTE:");
                break;
            case "Podcast":
                change_resenia.setText("EPISODIOS:");
                change_anio.setText("TEMPORADAS:");
                change_cantante.setText("");
                break;
            default:
                break;
        }
    }//GEN-LAST:event_cbo_searchActionPerformed

    private void btn_searchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_searchMouseClicked
        // TODO add your handling code here:
        String searchType = cbo_search.getSelectedItem().toString();

        switch (searchType) {
            case "Canción":
                mostrarResultadoCancion();
                break;
            case "Podcast":
                mostrarResultadoPodcast();
                break;
            default:
                JOptionPane.showMessageDialog(this, "Seleccione tipo de busqueda");
                break;
        }
    }//GEN-LAST:event_btn_searchMouseClicked


