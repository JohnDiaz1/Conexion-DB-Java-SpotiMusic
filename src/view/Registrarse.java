    private void btn_registerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_registerMouseClicked
        // TODO add your handling code here:
        login = new LoginDAO();
        cliente = new ClienteModel(username_field.getText(), name_field.getText(),
                lastName_field.getText(), email_field.getText(),
                cel_field.getText(), password_field.getText(),
                dateBorn_field.getText());
        String registrado = login.agregarUsuario(cliente);

        if (registrado != null) {
            JOptionPane.showMessageDialog(this, registrado);
        } else {
            JOptionPane.showMessageDialog(this, registrado);
        }

    }//GEN-LAST:event_btn_registerMouseClicked

